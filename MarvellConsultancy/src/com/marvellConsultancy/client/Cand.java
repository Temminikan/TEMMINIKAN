     /**
      * gets data from the database and adds to the table
      */
      private void getData(String databasetable, JTable tableName){
        try {
               Controller control = new Controller();
               Connection conn = control.getConnection() ;
                Statement stm = conn.createStatement();
                ResultSet rs = stm.executeQuery("SELECT * FROM "+databasetable+"");
                tableName.setModel(DbUtils.resultSetToTableModel(rs));
            }
            catch (SQLException ex) {
            Logger.getLogger(Controller.class.getName()).log(Level.SEVERE, null, ex);
        }
      }
     
     
